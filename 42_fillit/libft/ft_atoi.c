int		ft_atoi(const char *str)
{
	unsigned long			res;
	int						n;
	int						i;
	int						j;

	i = 0;
	j = -1;
	n = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
		n = ((str[i++] == '-') ? -1 : 1);
	while (str[i + ++j] >= '0' && str[i + j] <= '9')
		res = res * 10 + (str[i + j] - 48);
	if (res > 9223372036854775807 || j >= 20)
		return ((n == -1) ? 0 : -1);
	return (res * n);
}
